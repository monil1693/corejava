package practicefindbyroll;

public class MarksheetBean {
	private int id = 0;
	private String name = null;
	private int physics = 0;
	private int maths = 0 ;
	private int chmeistry=0;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPhysics() {
		return physics;
	}
	public void setPhysics(int physics) {
		this.physics = physics;
	}
	public int getMaths() {
		return maths;
	}
	public void setMaths(int maths) {
		this.maths = maths;
	}
	public int getChmeistry() {
		return chmeistry;
	}
	public void setChmeistry(int chmeistry) {
		this.chmeistry = chmeistry;
	}
	

}
