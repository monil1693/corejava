package rays;

public class relational {

	public static void main(String[] args) {
		int a =10;
		int b =21;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println((a>=b)&&(a<b));
		System.out.println((a<=b)||(a>=b));

	}

}
